package com.xworkz.studio.boot;

public class Almond {
	
	public String brand;
	public int cost;
	public double weight;
	public float rating;
	public boolean isOnSale;
	public char types;
	public Walnuts walnuts;
	
	
	
	public String getBrand() {
		return brand;
	}
	public int getCost() {
		return cost;
	}
	public double getWeight() {
		return weight;
	}
	public float getRating() {
		return rating;
	}
	public boolean isOnSale() {
		return isOnSale;
	}
	public char getTypes() {
		return types;
	}
	public Walnuts getWalnuts() {
		return walnuts;
	}
	
	
	
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public void setRating(float rating) {
		this.rating = rating;
	}
	public void setOnSale(boolean isOnSale) {
		this.isOnSale = isOnSale;
	}
	public void setTypes(char types) {
		this.types = types;
	}
	public void setWalnuts(Walnuts walnuts) {
		this.walnuts = walnuts;
	}
		

}
