package com.xworkz.cloths.boot;

public class Jeans {
	
	public String brand;
	public int cost;
	public char size;
	
	
	public String getBrand() {
		return brand;
	}
	public int getCost() {
		return cost;
	}
	public char getSize() {
		return size;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public void setSize(char size) {
		this.size = size;
	}
	
	

}
