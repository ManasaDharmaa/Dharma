package com.xworkz.seeds.boot;

public class Seeds {
	
	public String name;
	public int cost;
	public double quantity;
	public boolean isSeedlings;
	
	
	public String getName() {
		return name;
	}
	public int getCost() {
		return cost;
	}
	public double getQuantity() {
		return quantity;
	}
	public boolean isSeedlings() {
		return isSeedlings;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public void setQuantity(double quantity) {
		this.quantity = quantity;
	}
	public void setSeedlings(boolean isSeedlings) {
		this.isSeedlings = isSeedlings;
	}
	
	

}
