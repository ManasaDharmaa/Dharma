package com.xworkz.Diploma.boot;

public class Engineering extends Diploma{
	
	public String totalBranch;
	public int totalSection;
	public double noOfFacultes;
	public float noOfStudentsOfBranch;
	public boolean isDaycollege;
	
	
	public String getTotalBranch() {
		return totalBranch;
	}
	public int getTotalSection() {
		return totalSection;
	}
	public double getNoOfFacultes() {
		return noOfFacultes;
	}
	public float getNoOfStudentsOfBranch() {
		return noOfStudentsOfBranch;
	}
	public boolean isDaycollege() {
		return isDaycollege;
	}
	public void setTotalBranch(String totalBranch) {
		this.totalBranch = totalBranch;
	}
	public void setTotalSection(int totalSection) {
		this.totalSection = totalSection;
	}
	public void setNoOfFacultes(double noOfFacultes) {
		this.noOfFacultes = noOfFacultes;
	}
	public void setNoOfStudentsOfBranch(float noOfStudentsOfBranch) {
		this.noOfStudentsOfBranch = noOfStudentsOfBranch;
	}
	public void setDaycollege(boolean isDaycollege) {
		this.isDaycollege = isDaycollege;
	}
	
	

}
