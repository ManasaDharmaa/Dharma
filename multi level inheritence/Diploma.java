package com.xworkz.Diploma.boot;

public class Diploma {
	
	public String branch;
	public int section;
	public double noOfFaculty;
	public float noOfStudents;
	public boolean isHoliday;
	
	
	public String getBranch() {
		return branch;
	}
	public int getSection() {
		return section;
	}
	public double getNoOfFaculty() {
		return noOfFaculty;
	}
	public float getNoOfStudents() {
		return noOfStudents;
	}
	public boolean isHoliday() {
		return isHoliday;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public void setSection(int section) {
		this.section = section;
	}
	public void setNoOfFaculty(double noOfFaculty) {
		this.noOfFaculty = noOfFaculty;
	}
	public void setNoOfStudents(float noOfStudents) {
		this.noOfStudents = noOfStudents;
	}
	public void setHoliday(boolean isHoliday) {
		this.isHoliday = isHoliday;
	}
	
	
	
	
	

}
